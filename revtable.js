// 3 tables in Reverse order
console.log("3 tables in reverse order");
function revTable() {
    for (let i = 30; i > 0; i--) {
        if (i % 3 == 0) {
            console.log(i)
        }
    }
}
revTable()

// 5 tables in Reverse order
console.log("5 tables in reverse order");
function revTables() {
    for (let i = 50; i > 0; i--) {
        if (i % 5 == 0) {
            console.log(i)
        }
    }
}
revTables()
